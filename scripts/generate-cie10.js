// Create the derm CIE10 database.
// To ensure >=250 real-like entries we will map out the L00-L99 and C/D codes.

const db = [];

function addEntries(groupLabel, groupRange, codes) {
    for (const [codeStr, desc] of Object.entries(codes)) {
        // Some codes are objects with subcodes, some are strings
        if (typeof desc === 'string') {
            db.push({
                code: codeStr.replace('.', '').toUpperCase(),
                label: desc.toUpperCase(),
                group: groupRange,
                groupLabel: groupLabel.toUpperCase()
            });
        }
    }
}

